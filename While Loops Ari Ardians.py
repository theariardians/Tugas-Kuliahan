i = 1 #variabel i
while i < 9: #dapat mengeksekusi satu set pernyataan variabel i selama kondisinya benar
  print(i) #perintah untuk variabel i
  i += 1 #perintah untuk menaikkan nilai variabel i sebanyak 1 angka dalam setiap perulangan


a = 1 #variabel a
while a < 8: #dapat mengeksekusi satu set pernyataan variabel a selama kondisinya benar
  print(a) #perintah untuk variabel a
  if a == 7:
    break #dapat menghentikan perulangan meskipun kondisi while benar
  a += 1 #perintah untuk menaikkan nilai variabel a sebanyak 1 angka dalam setiap perulangan


y = 1 #variabel y
while y < 9: #dapat mengeksekusi satu set pernyataan variabel y selama kondisinya benar
  y += 1 #perintah untuk menaikkan nilai variabel i sebanyak 1 angka dalam setiap perulangan
  if y == 8:
    continue #dapat menghentikan iterasi saat ini, dan melanjutkan dengan yang continue
  print(y) #printah untuk variabel y